num=int(input("Enter any Number : "))
if num==0:
      print("Zero")
elif num>0:
      print("Number is positive")
      if num%2==0:
            print("Number is Even ")
      else:
            print("Number is Odd")
else:
      print("Number is Negative ")
      if num%2==0:
            print("Number is Even ")
      else:
            print("Number is Odd")
            
